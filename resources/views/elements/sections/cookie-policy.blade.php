                    <h2><b>Çerez Nedir?</b></h2>
                    <p>Çerez, bir web sitesi sunucusu tarafından bilgisayarınıza veya mobil cihazınıza kaydedilen basit bir
                        metin dosyasıdır. Sadece bu sunucu ilgili çerezin içeriğine erişebilir veya bu bilgileri okuyabilir.</p>
                    <p>Her çerez, web tarayıcınıza özeldir. Bir benzersiz tanımlayıcı, site adı ve bazı numaralar gibi anonim
                        bilgiler içerir. Web sitesinin bazı öğeleri, örneğin tercihlerinizi hatırlamasını sağlar.</p>
                    <p>Çerezler, ziyaret ettiğiniz web sitesi (bunlar “birinci taraf çerezler” olarak bilinir) veya
                        görüntülediğiniz sayfada içerik sunan diğer web siteleri (“üçüncü taraf çerezler”) tarafından
                        belirlenebilir.</p>
                    <p>Çerezler hakkında daha fazla bilgi edinmek istiyorsanız HowStuffWorks web sitesi kapsamlı bir kılavuz
                        sağlamaktadır:&nbsp;<a title="how works cookie" href="http://computer.howstuffworks.com/cookie.htm">İnternet Çerezleri Nasıl Çalışır?</a></p>
                    <h2><b>Çerezleri Etkinleştirme/Devre Dışı Bırakma</b></h2>
                    <p>Web sitemizi kullanarak, çerezlerin cihazınızda kullanılmasına ve cihazınıza kaydedilmesine onay
                        verirsiniz. Web sitemizi çerezler olmadan kullanabilirsiniz ancak bunun sonucunda yararlanabileceğiniz
                        hizmetler kısıtlı olabilir.</p>
                    <p>Tarayıcınızı çerezleri kabul etmemek üzere ayarlayarak çerezlerin cihazınıza kaydedilmesini
                        önleyebilirsiniz. Çoğu tarayıcının varsayılan ayar olarak çerezleri kabul ettiğini lütfen unutmayın.</p>
                    <p>Ayrıca istediğiniz zaman cihazınızda kayıtlı çerezleri silebilirsiniz. Bu işlemi nasıl yapabileceğinizle
                        ilgili talimatlar, tarayıcınızın veya cihazınızın kılavuzunda bulunabilir.</p>
                    <p><strong>Çerezleri Devredışı Bırakma Yöntemleri</strong></p>
                    <p>İnternet tarayıcınızın çeşidine göre aşağıdaki adımları izleyerek, çerezler hakkında bilgi edinip, izin
                        verme veya reddetme hakkınızı kullanabilirsiniz:</p>
                    <p>Chrome (Bilgisayar, Android, iPhone ve iPad):&nbsp;<a title="Chrome (Bilgisayar, Android, iPhone ve iPad)" href="https://support.google.com/chrome/answer/95647">https://support.google.com/chrome/answer/95647</a>
                    </p>
                    <p>Mozilla Firefox:&nbsp;<a title="Mozilla Firefox" href="https://support.mozilla.org/tr/kb/web-sitesi-tercihleri-cerezleri-acip-kapatma">https://support.mozilla.org/tr/kb/web-sitesi-tercihleri-cerezleri-acip-kapatma</a>
                    </p>
                    <p>Internet Explorer:&nbsp;<a title="Internet Explorer" href="https://support.microsoft.com/tr-tr/help/17442/windows-internet-explorer-delete-manage-cookies">https://support.microsoft.com/tr-tr/help/17442/windows-internet-explorer-delete-manage-cookies</a>
                    </p>
                    <p>Safari (Bilgisayar):&nbsp;<a title="Safari (Bilgisayar)" href="https://support.apple.com/tr-tr/guide/safari/sfri11471/mac">https://support.apple.com/tr-tr/guide/safari/sfri11471/mac</a>
                    </p>
                    <p>Safari (Telefon): Telefonunuzun “Ayarlar” bölümünden “safari” sekmesini seçip, “Gizlilik ve Güvenlik”
                        Bölümünden tüm çerez yönetiminizi sağlayabilirsiniz.</p>
                    <p>Diğer tarayıcılar için de (opera, microsoft edge gibi), ilgili tarayıcıların yardım veya destek
                        sayfalarını inceleyebilirsiniz.</p>
                    <p><strong>Çerezlerin Devredışı Bırakılması Halinde:</strong></p>
                    <p>Sitemiz’in operasyonel olarak gerekli olan çerezleri veya işlevselliğe dayalı olan çerezleri
                        reddederseniz, web sitesini, mobil uygulamayı ve mobil sitesini kullanmaya devam edebilirsiniz fakat web
                        sitesinin, mobil uygulamanın ve mobil sitesinin tüm işlevlerine erişemeyebilirsiniz veya erişiminiz
                        sınırlı olabilir.</p>
